# Estruturas de repetição

# while: repete um bloco de código enquanto uma condição for verdadeira (True). É ideal para repetições baseadas em condições, como contagens, validação de entrada de usuário ou leitura de dados até que um valor específico (como 0 ou stop) seja inserido.

i = 1
while i <=5:
    print('*' * i)
    i = i + 1

print('Terminou!')