# Continuação de Estruturas de Repetição

# for: é usado para iterar sobre sequências (listas, tuplas, strings) ou intervalos numéricos, executando um bloco de código para cada item. A sintaxe básica é for item in sequencia:, seguida pelo código indentado. É uma estrutura fundamental para automação e manipulação de dados. 
# range: gera uma sequência de números inteiros, sendo comumente usada em loops for.

for item in ['banana', 'maçã', 'abacate']:
    print(item)

for item in range(10):
    print(item)

for item in range(1,11):
    print(item)

for item in range(1,11,2):
    print(item)